package com.amy.sort;

public class SortUtils {
	
	public static void printArray(int x[]) {
		System.out.println("---------Printing-----------");
		for (int i = 0; i < x.length; i++) {

			System.out.println("| " + i + "| " + x[i] + "|");

		}
		System.out.print("---------PrintingEnd---------");
	}

}
